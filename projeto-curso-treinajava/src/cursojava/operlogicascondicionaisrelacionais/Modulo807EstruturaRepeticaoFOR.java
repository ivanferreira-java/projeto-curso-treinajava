package cursojava.operlogicascondicionaisrelacionais;

public class Modulo807EstruturaRepeticaoFOR {
	
	public static void main(String[] args) {
		
		for (int numero = 0; numero <= 10; numero++) {
			System.out.println("Número atual é: " + numero);
		}
		
		System.out.println("-----------------------------------------------------------------------------------------");
		
		for (int numero2 = 10; numero2 >= 0; numero2--) {
			System.out.println("Número atual é: " + numero2);
		}
		
	}

}
